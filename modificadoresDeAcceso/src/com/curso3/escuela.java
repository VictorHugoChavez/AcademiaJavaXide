package com.curso3;

public class escuela {
	private int boleta, grupo;
	private String escuela;
	private boolean relugar;
	public int getBoleta() {
		return boleta;
	}
	public void setBoleta(int boleta) {
		this.boleta = boleta;
	}
	public int getGrupo() {
		return grupo;
	}
	public void setGrupo(int grupo) {
		this.grupo = grupo;
	}
	public String getEscuela() {
		return escuela;
	}
	public void setEscuela(String escuela) {
		this.escuela = escuela;
	}
	public boolean isRelugar() {
		return relugar;
	}
	public void setRelugar(boolean relugar) {
		this.relugar = relugar;
	}
	
	
}
